def arithmetic_ops():
    a = float(input("Enter first number: "))
    b = float(input("Enter second number: "))
    print(f"Addition: {a + b}")
    print(f"Subtraction: {a - b}")
    print(f"Multiplication: {a * b}")
    print(f"Division: {a / b}")
    print(f"Modulus: {a % b}")

arithmetic_ops()
